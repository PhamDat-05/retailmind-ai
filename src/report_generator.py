def build_methodology_section():
    """
    Build methodology note section.
    """
    return """## 9. Methodology Note

RetailMind AI does not send the raw CSV file directly to the AI model.

Instead, the application follows this workflow:

1. Upload CSV file
2. Detect possible schema mappings
3. Let the user confirm business-field mappings
4. Standardize the dataset into an internal analytics schema
5. Clean and enrich the data
6. Calculate dynamic KPIs and segment summaries
7. Build a structured AI context with data availability metadata
8. Send only the structured context and recent chat history to the AI model
9. Generate business insights, strategic recommendations, and natural-language answers

This design helps reduce hallucination by grounding AI output in calculated metrics and clearly identifying unavailable fields.
"""


def build_limitations_section(field_availability):
    """
    Build limitations section.
    """
    lines = []

    lines.append("## 10. Data Limitations")
    lines.append("")

    limitations = []

    if field_availability:
        if not field_availability.get("profit", {}).get("available", False):
            limitations.append(
                "Profit / gross income data is not available, so profitability analysis is limited."
            )

        if not field_availability.get("rating", {}).get("available", False):
            limitations.append(
                "Rating data is not available, so customer satisfaction analysis is limited."
            )

        if not field_availability.get("payment", {}).get("available", False):
            limitations.append(
                "Payment method data is not available, so payment behavior analysis is limited."
            )

        if not field_availability.get("customer_type", {}).get("available", False):
            limitations.append(
                "Customer type / segment data is not available, so customer segmentation analysis is limited."
            )

        if not field_availability.get("branch", {}).get("available", False):
            limitations.append(
                "Branch / store data is not available, so branch-level comparison is limited."
            )

        if not field_availability.get("city", {}).get("available", False):
            limitations.append(
                "City / region data is not available, so regional comparison is limited."
            )

    if limitations:
        for limitation in limitations:
            lines.append(f"- {limitation}")
    else:
        lines.append("- No major optional field limitations detected.")

    return "\n".join(lines)


