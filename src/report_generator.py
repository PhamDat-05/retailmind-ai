def build_dataset_overview_section(
    raw_df,
    df_cleaned,
    df_filtered
):
    """
    Build dataset overview section.
    """
    original_rows = raw_df.shape[0] if raw_df is not None else 0
    original_columns = raw_df.shape[1] if raw_df is not None else 0
    standardized_rows = df_cleaned.shape[0] if df_cleaned is not None else 0
    filtered_rows = df_filtered.shape[0] if df_filtered is not None else 0

    missing_values = int(raw_df.isnull().sum().sum()) if raw_df is not None else 0
    duplicate_rows = int(raw_df.duplicated().sum()) if raw_df is not None else 0

    return f"""## 1. Dataset Overview

- Original rows: {original_rows:,}
- Original columns: {original_columns:,}
- Standardized rows: {standardized_rows:,}
- Filtered rows used in this report: {filtered_rows:,}
- Original missing values: {missing_values:,}
- Original duplicate rows: {duplicate_rows:,}
"""


def build_field_availability_section(field_availability):
    """
    Build field availability section.
    """
    available_fields, unavailable_fields = get_available_unavailable_fields(
        field_availability
    )

    lines = []
    lines.append("## 2. Field Availability")
    lines.append("")

    lines.append("### Available Fields")
    if available_fields:
        for field in available_fields:
            lines.append(f"- {field}")
    else:
        lines.append("- Not specified")

    lines.append("")

    lines.append("### Unavailable Fields")
    if unavailable_fields:
        for field in unavailable_fields:
            lines.append(f"- {field}")
    else:
        lines.append("- None")

    return "\n".join(lines)


