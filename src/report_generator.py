def generate_markdown_report(
    raw_df,
    df_cleaned,
    df_filtered,
    overall_kpis,
    top_performers,
    segment_summaries,
    field_availability,
    ai_insights=None,
    ai_recommendations=None,
    ai_context_text=None,
    chat_history=None
):
    """
    Generate a complete Markdown business report.
    """
    sections = []

    sections.append(build_report_metadata_section())
    sections.append(build_dataset_overview_section(raw_df, df_cleaned, df_filtered))
    sections.append(build_field_availability_section(field_availability))
    sections.append(build_kpi_summary_section(overall_kpis))
    sections.append(build_top_performers_section(top_performers))
    sections.append(build_segment_tables_section(segment_summaries))
    sections.append(build_ai_insights_section(ai_insights))
    sections.append(build_ai_recommendations_section(ai_recommendations))
    sections.append(build_chat_history_section(chat_history))
    sections.append(build_methodology_section())
    sections.append(build_limitations_section(field_availability))

    if ai_context_text:
        sections.append("## 11. Structured AI Context")
        sections.append("")
        sections.append("```text")
        sections.append(ai_context_text)
        sections.append("```")

    return "\n\n".join(sections)


