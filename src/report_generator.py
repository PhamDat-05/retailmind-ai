def build_segment_tables_section(segment_summaries):
    """
    Build segment summary tables section.
    """
    lines = []

    lines.append("## 5. Segment Summary Tables")
    lines.append("")

    segment_config = [
        ("Revenue by Product / Category", "revenue_by_product_line"),
        ("Revenue by City / Region", "revenue_by_city"),
        ("Revenue by Branch / Store", "revenue_by_branch"),
        ("Revenue by Customer Type / Segment", "revenue_by_customer_type"),
        ("Revenue by Payment Method", "revenue_by_payment"),
        ("Revenue by Date", "revenue_by_date"),
    ]

    for title, key in segment_config:
        lines.append(f"### {title}")
        lines.append("")

        df = segment_summaries.get(key) if segment_summaries else None

        if df is not None and not df.empty:
            lines.append(dataframe_to_markdown_table(df, max_rows=10))
        else:
            lines.append("Not available in uploaded dataset")

        lines.append("")

    return "\n".join(lines)


