def format_currency(value):
    """
    Format numeric value as currency.
    """
    if value is None or pd.isna(value):
        return "Not available"

    return f"${value:,.2f}"


def format_number(value, decimals=2):
    """
    Format numeric value safely.
    """
    if value is None or pd.isna(value):
        return "Not available"

    return f"{value:,.{decimals}f}"


def format_plain(value):
    """
    Format plain text safely.
    """
    if value is None or pd.isna(value):
        return "Not available"

    return str(value)


def escape_markdown_cell(value):
    """
    Convert cell value to safe Markdown table text.
    This avoids dependency on pandas.to_markdown() and tabulate.
    """
    if value is None or pd.isna(value):
        return "N/A"

    if isinstance(value, float):
        return f"{value:,.2f}"

    if isinstance(value, int):
        return f"{value:,}"

    text = str(value)
    text = text.replace("|", "\\|")
    text = text.replace("\n", " ")

    return text


def get_available_unavailable_fields(field_availability):
    """
    Split field availability metadata into available and unavailable field lists.
    """
    available_fields = []
    unavailable_fields = []

    if not field_availability:
        return available_fields, unavailable_fields

    for _, info in field_availability.items():
        label = info.get("label", "Unknown field")
        mapped_column = info.get("mapped_column")

        if info.get("available"):
            available_fields.append(
                f"{label} — mapped from `{mapped_column}`"
            )
        else:
            unavailable_fields.append(label)

    return available_fields, unavailable_fields


def dataframe_to_markdown_table(df, max_rows=10):
    """
    Convert a dataframe into a Markdown table without requiring tabulate.
    """
    if df is None or df.empty:
        return "Not available"

    display_df = df.head(max_rows).copy()

    if display_df.empty:
        return "Not available"

    columns = list(display_df.columns)

    header = "| " + " | ".join(columns) + " |"
    separator = "| " + " | ".join(["---"] * len(columns)) + " |"

    rows = []

    for _, row in display_df.iterrows():
        row_values = [
            escape_markdown_cell(row[col])
            for col in columns
        ]
        rows.append("| " + " | ".join(row_values) + " |")

    return "\n".join([header, separator] + rows)


